import Handlebars from 'handlebars';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import type { DomainMetadata, FieldMetadata } from '../../models/index.js';
import { DslMapper } from '../../models/index.js';
import type { GeneratorConfig } from '../../config.js';
 import type { CodeGenerator, GeneratedFile, GeneratorContext } from '../../core/generator-registry.js';
import type { BackendType } from '../../core/backend-strategy.js';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Register Handlebars helpers
Handlebars.registerHelper('eq', (a, b) => a === b);
Handlebars.registerHelper('kebabCase', (str: string) => DslMapper.toKebabCase(str));
Handlebars.registerHelper('camelCase', (str: string) => DslMapper.toCamelCase(str));
Handlebars.registerHelper('pascalCase', (str: string) => str.charAt(0).toUpperCase() + str.slice(1));

export { GeneratedFile };

export class ServiceGenerator implements CodeGenerator {
  readonly name = 'ServiceGenerator';
  readonly artifactType = 'SERVICE' as const;
  readonly supportedBackends: BackendType[] = [];
  readonly priority = 5;

  generate(domain: DomainMetadata, context: GeneratorContext): GeneratedFile | null {
    if (domain.internalApi?.hidden) {
      return null;
    }

    const content = this.generateServiceContent(domain, context);
    const fileName = `${DslMapper.toKebabCase(domain.entityName)}.service.ts`;
    const filePath = join('services', fileName);

    return {
      path: filePath,
      content,
      artifactType: 'SERVICE',
      overwritable: true,
    };
  }

  generateAggregate(domains: DomainMetadata[], context: GeneratorContext): GeneratedFile[] {
    const visibleDomains = domains.filter(d => !d.internalApi?.hidden);
    return [
      {
        path: 'services/index.ts',
        content: this.generateServicesBarrel(visibleDomains),
        artifactType: 'SERVICE',
        overwritable: true,
      },
    ];
  }

  private generateServicesBarrel(domains: DomainMetadata[]): string {
    const lines: string[] = [
      `/**`,
      ` * Services - Barrel Export`,
      ` * Generated by @exeris/codegen-ts v0.4.0`,
      ` */`,
      ``,
    ];

    for (const domain of domains) {
      const kebab = DslMapper.toKebabCase(domain.entityName);
      lines.push(`export * from './${kebab}.service';`);
    }

    return lines.join('\n');
  }

  private generateServiceContent(domain: DomainMetadata, context: GeneratorContext): string {
    const metadata = domain;
    const entityName = metadata.entityName;
    const kebabName = DslMapper.toKebabCase(entityName);

    // Build API path: prefer explicit apiPath, otherwise construct from apiVersion + path
    // Backend uses: /api/v1/tenants (apiBasePath + apiVersion + path)
    // Default fallback: /{entity}s (pluralized entity name)
    const apiVersion = metadata.apiVersion ?? '';
    const pathSegment = metadata.path ?? `/${kebabName}s`;
    const apiPath = metadata.apiPath ?? (apiVersion ? `/${apiVersion}${pathSegment}` : pathSegment);

    // Collect enum types for imports
    const enumTypes = this.collectEnumTypes(metadata.fields);

    const fields = metadata.fields.map((field) => {
      const mapping = DslMapper.mapType(field.type);
      return {
        name: field.name,
        tsType: mapping.tsType,
        zodType: this.buildZodType(field),
        required: field.required,
      };
    });

    const filterableFields = metadata.fields
      .filter((f) => f.filterable)
      .map((field) => {
        const mapping = DslMapper.mapType(field.type);
        return {
          name: field.name,
          filterType: `${mapping.tsType} | undefined`,
        };
      });

    // T1: actions are served at POST {base}/{id}/actions/{kebab(name)} (matches the
    // OpenAPI path + the kernel route), and the server responds with the updated
    // aggregate — so the client method takes the entity id and returns the entity.
    const actions = (metadata.actions ?? []).map((action) => ({
      name: action.name,
      description: action.description ?? `Execute ${action.name} action`,
      // methodName: valid camelCase JS identifier (kebab/snake action names would
      // otherwise emit invalid syntax); kebabName: the URL segment, matching the kernel route.
      methodName: DslMapper.toMethodName(action.name),
      kebabName: DslMapper.toKebabCase(action.name),
      hasParams: action.params && action.params.length > 0,
      params: (action.params ?? []).map((p) => ({
        name: p.name,
        tsType: DslMapper.mapType(p.type).tsType,
      })),
    }));

    const systemFields = this.getSystemFields(metadata);

    // Generate service content inline (without template file for reliability)
    return this.renderService({
      entityName,
      apiPath,
      apiBasePath: context.config.apiBasePath,
      generateZod: context.config.generateZod,
      softDelete: metadata.softDelete,
      fields,
      filterableFields,
      actions,
      systemFields,
      enumTypes,
    });
  }

  private renderService(data: Record<string, unknown>): string {
    const { entityName, apiBasePath, apiPath, generateZod, softDelete, fields, filterableFields, actions, systemFields, enumTypes } = data as {
      entityName: string;
      apiBasePath: string;
      apiPath: string;
      generateZod: boolean;
      softDelete: boolean;
      fields: Array<{ name: string; tsType: string; zodType: string; required: boolean }>;
      filterableFields: Array<{ name: string; filterType: string }>;
      actions: Array<{ name: string; description: string; methodName: string; kebabName: string; hasParams: boolean; params: Array<{ name: string; tsType: string }> }>;
      systemFields: string[];
      enumTypes: string[];
    };

    const kebabName = entityName.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase();
    const lines: string[] = [];

    // Header
    lines.push(`/**`);
    lines.push(` * ${entityName} API Service`);
    lines.push(` * Generated by @exeris/codegen-ts v0.2.0`);
    lines.push(` * DO NOT EDIT - This file is auto-generated`);
    lines.push(` */`);
    lines.push(``);
    lines.push(`import { Injectable, inject } from '@angular/core';`);
    lines.push(`import { HttpClient, HttpParams } from '@angular/common/http';`);
    lines.push(`import { Observable } from 'rxjs';`);
    lines.push(``);

    // Import types from types file
    lines.push(`import type { ${entityName}, ${entityName}Create, ${entityName}Update } from '../types/${kebabName}.types';`);

    // Add enum imports if needed
    if (enumTypes && enumTypes.length > 0) {
      const typeImports = enumTypes.join(', ');
      lines.push(`import type { ${typeImports} } from '../types/enums';`);
    }
    lines.push(``);

    // Re-export types for convenience
    lines.push(`// Re-export types for convenience`);
    lines.push(`export type { ${entityName}, ${entityName}Create, ${entityName}Update } from '../types/${kebabName}.types';`);
    lines.push(``);

    // Page and PageRequest interfaces (only define once, not duplicated)
    lines.push(`// ============================================================================`);
    lines.push(`// Pagination Types`);
    lines.push(`// ============================================================================`);
    lines.push(``);

    // Page interface
    lines.push(`export interface Page<T> {`);
    lines.push(`  content: T[];`);
    lines.push(`  totalElements: number;`);
    lines.push(`  totalPages: number;`);
    lines.push(`  size: number;`);
    lines.push(`  number: number;`);
    lines.push(`  first: boolean;`);
    lines.push(`  last: boolean;`);
    lines.push(`}`);
    lines.push(``);

    lines.push(`export interface PageRequest {`);
    lines.push(`  page?: number;`);
    lines.push(`  size?: number;`);
    lines.push(`  sort?: string;`);
    lines.push(`  direction?: 'asc' | 'desc';`);
    lines.push(`}`);
    lines.push(``);

    // Filter interface
    lines.push(`export interface ${entityName}Filter {`);
    for (const ff of filterableFields) {
      lines.push(`  ${ff.name}?: ${ff.filterType};`);
    }
    lines.push(`  search?: string;`);
    lines.push(`}`);
    lines.push(``);

    // Service
    lines.push(`// ============================================================================`);
    lines.push(`// Service`);
    lines.push(`// ============================================================================`);
    lines.push(``);
    lines.push(`@Injectable({ providedIn: 'root' })`);
    lines.push(`export class ${entityName}Service {`);
    lines.push(`  private readonly http = inject(HttpClient);`);
    lines.push(`  private readonly baseUrl = '${apiBasePath}${apiPath}';`);
    lines.push(``);
    lines.push(`  findAll(pageRequest: PageRequest = {}, filter: ${entityName}Filter = {}): Observable<Page<${entityName}>> {`);
    lines.push(`    let params = new HttpParams();`);
    lines.push(`    if (pageRequest.page !== undefined) params = params.set('page', pageRequest.page.toString());`);
    lines.push(`    if (pageRequest.size !== undefined) params = params.set('size', pageRequest.size.toString());`);
    lines.push(`    if (pageRequest.sort) params = params.set('sort', \`\${pageRequest.sort},\${pageRequest.direction ?? 'asc'}\`);`);
    lines.push(`    Object.entries(filter).forEach(([key, value]) => {`);
    lines.push(`      if (value !== undefined && value !== null && value !== '') {`);
    lines.push(`        params = params.set(key, String(value));`);
    lines.push(`      }`);
    lines.push(`    });`);
    lines.push(`    return this.http.get<Page<${entityName}>>(this.baseUrl, { params });`);
    lines.push(`  }`);
    lines.push(``);
    lines.push(`  findById(id: string): Observable<${entityName}> {`);
    lines.push(`    return this.http.get<${entityName}>(\`\${this.baseUrl}/\${id}\`);`);
    lines.push(`  }`);
    lines.push(``);
    lines.push(`  create(data: ${entityName}Create): Observable<${entityName}> {`);
    lines.push(`    return this.http.post<${entityName}>(this.baseUrl, data);`);
    lines.push(`  }`);
    lines.push(``);
    lines.push(`  update(id: string, data: ${entityName}Update): Observable<${entityName}> {`);
    lines.push(`    return this.http.patch<${entityName}>(\`\${this.baseUrl}/\${id}\`, data);`);
    lines.push(`  }`);
    lines.push(``);
    lines.push(`  delete(id: string): Observable<void> {`);
    lines.push(`    return this.http.delete<void>(\`\${this.baseUrl}/\${id}\`);`);
    lines.push(`  }`);

    if (softDelete) {
      lines.push(``);
      lines.push(`  softDelete(id: string): Observable<void> {`);
      lines.push(`    return this.http.patch<void>(\`\${this.baseUrl}/\${id}/archive\`, {});`);
      lines.push(`  }`);
      lines.push(``);
      lines.push(`  restore(id: string): Observable<${entityName}> {`);
      lines.push(`    return this.http.patch<${entityName}>(\`\${this.baseUrl}/\${id}/restore\`, {});`);
      lines.push(`  }`);
    }

    // Custom actions (T1) — POST {baseUrl}/{id}/actions/{kebab(name)}, returns the
    // updated aggregate (the server responds with the persisted entity).
    for (const action of actions) {
      lines.push(``);
      lines.push(`  /** ${action.description} */`);
      const paramsStr = action.hasParams
        ? ', ' + action.params.map(p => `${p.name}: ${p.tsType}`).join(', ')
        : '';
      const bodyParams = action.hasParams
        ? `{ ${action.params.map(p => p.name).join(', ')} }`
        : '{}';
      lines.push(`  ${action.methodName}(id: string${paramsStr}): Observable<${entityName}> {`);
      lines.push(`    return this.http.post<${entityName}>(\`\${this.baseUrl}/\${id}/actions/${action.kebabName}\`, ${bodyParams});`);
      lines.push(`  }`);
    }

    lines.push(`}`);

    return lines.join('\n');
  }

  private buildZodType(field: FieldMetadata): string {
    let zodType = DslMapper.mapType(field.type).zodType;

    if (field.minLength && zodType.includes('z.string')) {
      zodType = zodType.replace('z.string()', `z.string().min(${field.minLength})`);
    }
    if (field.maxLength && zodType.includes('z.string')) {
      zodType = `${zodType}.max(${field.maxLength})`;
    }
    if (field.format === 'email') {
      zodType = 'z.string().email()';
    }
    if (field.format === 'url') {
      zodType = 'z.string().url()';
    }

    if (!field.required) {
      zodType += '.optional()';
    }

    return zodType;
  }

  private getSystemFields(metadata: DomainMetadata): string[] {
    const fields = ['id'];
    const sf = metadata.systemFields;

    if (sf) {
      if (sf.versionField) fields.push(sf.versionField);
      if (sf.createdAtField) fields.push(sf.createdAtField);
      if (sf.updatedAtField) fields.push(sf.updatedAtField);
      if (sf.createdByField) fields.push(sf.createdByField);
      if (sf.updatedByField) fields.push(sf.updatedByField);
    } else {
      fields.push('createdAt', 'updatedAt', 'version');
    }

    return fields;
  }

  /**
   * Collects enum type names from fields.
   */
  private collectEnumTypes(fields: FieldMetadata[]): string[] {
    const enumTypes = new Set<string>();

    const knownJavaTypes = new Set([
      'String', 'java.lang.String',
      'Integer', 'java.lang.Integer', 'int',
      'Long', 'java.lang.Long', 'long',
      'Double', 'java.lang.Double', 'double',
      'Float', 'java.lang.Float', 'float',
      'Boolean', 'java.lang.Boolean', 'boolean',
      'BigDecimal', 'java.math.BigDecimal',
      'BigInteger', 'java.math.BigInteger',
      'UUID', 'java.util.UUID',
      'Instant', 'java.time.Instant',
      'LocalDate', 'java.time.LocalDate',
      'LocalDateTime', 'java.time.LocalDateTime',
      'LocalTime', 'java.time.LocalTime',
      'ZonedDateTime', 'java.time.ZonedDateTime',
      'Duration', 'java.time.Duration',
      'Period', 'java.time.Period',
      'Date', 'java.util.Date',
      'byte[]', 'Object', 'java.lang.Object',
    ]);

    for (const field of fields) {
      const javaType = field.type;

      if (field.enumType) {
        const simpleName = field.enumType.includes('.')
          ? field.enumType.substring(field.enumType.lastIndexOf('.') + 1)
          : field.enumType;
        enumTypes.add(simpleName);
        continue;
      }

      if (knownJavaTypes.has(javaType) || javaType.includes('<') || javaType.endsWith('[]')) {
        continue;
      }

      const simpleName = javaType.includes('.')
        ? javaType.substring(javaType.lastIndexOf('.') + 1)
        : javaType;

      if (knownJavaTypes.has(simpleName)) {
        continue;
      }

      if (/^[A-Z][a-zA-Z0-9]*$/.test(simpleName)) {
        if (simpleName.endsWith('Role') || simpleName.endsWith('Status') ||
            simpleName.endsWith('Type') || simpleName.endsWith('Plan') ||
            simpleName.endsWith('State') || simpleName.endsWith('Level') ||
            simpleName.endsWith('Kind') || simpleName.endsWith('Mode') ||
            simpleName.endsWith('Category') || javaType.includes('.domain.')) {
          enumTypes.add(simpleName);
        }
      }
    }

    return [...enumTypes];
  }
}

export function generateService(metadata: DomainMetadata, config: GeneratorConfig): GeneratedFile | null {
  const generator = new ServiceGenerator();
  const context: GeneratorContext = { config, backend: config.backend ?? 'KERNEL', allDomains: [metadata], enums: [] };
  return generator.generate(metadata, context);
}

