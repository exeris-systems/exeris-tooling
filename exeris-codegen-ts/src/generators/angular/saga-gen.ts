export type SagaState = 
  | 'IDLE' 
  | 'RUNNING' 
  | 'COMPLETED' 
  | 'FAILED' 
  | 'COMPENSATING'
  | 'COMPENSATED';

export type StepStatus = 
  | 'PENDING' 
  | 'RUNNING' 
  | 'COMPLETED' 
  | 'FAILED' 
  | 'COMPENSATING'
  | 'COMPENSATED'
  | 'SKIPPED';

export interface SagaStep {
  /** Step identifier */
  name: string;
  /** Human-readable label */
  label: string;
  /** Current status */
  status: StepStatus;
  /** Step started timestamp */
  startedAt?: Date;
  /** Step completed timestamp */
  completedAt?: Date;
  /** Error message if failed */
  error?: string;
  /** Compensation action name */
  compensatingAction?: string;
  /** Progress percentage (0-100) for long-running steps */
  progress?: number;
}

export interface SagaExecution {
  /** Execution ID */
  id: string;
  /** Entity ID this saga operates on */
  entityId: string;
  /** Overall saga state */
  state: SagaState;
  /** Individual step states */
  steps: SagaStep[];
  /** Saga start time */
  startedAt: Date;
  /** Saga completion time */
  completedAt?: Date;
  /** Error that caused failure */
  error?: string;
}

