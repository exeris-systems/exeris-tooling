export function generateSaga(
  domain: DomainMetadata,
  config: GeneratorConfig
): GeneratedFile | null {
  const generator = new SagaGenerator();
  const context: GeneratorContext = {
    config,
    backend: 'KERNEL',
    allDomains: [domain],
    enums: [],
  };

  return generator.generate(domain, context);
}

